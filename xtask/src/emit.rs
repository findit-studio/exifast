//! Render [`TableModel`]s as committed Rust source in the XMP module's
//! `Field::make(...)` vocabulary (`Writable as W` / `PrintConv as P`).
//!
//! Two entry points:
//!
//! * [`emit_table`] — one table → a `pub static FIELDS: &[Field]` (the
//!   single-namespace path the Task-5/6 unit tests pin).
//! * [`emit_xmp_tables`] — EVERY `XMP`-group table → one committed file with a
//!   `pub static GEN_<NS>: &[Field]` per namespace + a `GEN_NS_TABLES` index
//!   (the full XMP surface, Task 7).
//!
//! The emitted file is a child module of `src/formats/xmp/` so it
//! `use super::tables::{Field, PrintConv as P, Writable as W}` and the
//! crate-local `Field::make` constructor is in scope. A PrintConv value-map is
//! a sorted `&[(i64, &str)]` / `&[(&str, &str)]` slice const when SMALL, or a
//! `phf::Map<&'static str, &'static str>` (built by `phf_codegen`) when it has
//! more than [`PHF_THRESHOLD`] entries — both keyed by the RAW scalar string so
//! the `apply_print_conv` lookup is the faithful `$$conv{$val}` exact-string
//! match (ExifTool.pm:3604). A code-valued, map-less conv flagged by the
//! [`crate::conv_registry`] becomes `P::Unported("<module>:<Tag>")`.

use crate::conv_registry::{resolve_printconv, Conv};
use crate::listx::TableModel;
use std::fmt::Write as _;

/// Value-maps with MORE than this many entries are emitted as a
/// `phf::Map<&str, &str>` (O(1) lookup); smaller maps stay a sorted slice +
/// linear stringified scan (no perceptible cost, smaller codegen, and the
/// representation the hand-written tables already use). Across the full XMP
/// surface only 3 maps exceed 24 (PLUS `MediaSummaryCode` = 2143 entries,
/// `tiff:Compression` = 53, `exif:LightSource` = 32), so the threshold keeps
/// the generated file overwhelmingly slice-based while giving the one giant
/// string map an O(1) lookup. Resolves #46/FU-4 for the generated maps.
pub const PHF_THRESHOLD: usize = 24;

/// Render `model` as committed Rust source for the XMP module (single-table
/// path; the all-namespaces path is [`emit_xmp_tables`]).
pub fn emit_table(model: &TableModel) -> String {
  let mut out = String::new();
  out.push_str("// generated by xtask gen-tables from exiftool -listx 13.59 — DO NOT EDIT\n");
  let _ = writeln!(
    out,
    "// (regenerate with `cargo xtask gen-tables --module {}`)",
    module_arg(model)
  );
  out.push_str("// source table: ");
  out.push_str(&model.name);
  out.push('\n');
  out.push_str("\nuse super::tables::{Field, PrintConv as P, Writable as W};\n\n");

  let mut map_consts = String::new();
  let mut fields = String::new();
  for t in &model.tags {
    let p = field_print_conv(&model.name, t, &mut map_consts);
    let _ = writeln!(
      fields,
      "  Field::make(\"{}\", {}, {}, {}),",
      t.id,
      name_arg(t),
      writable_variant(t.ty.as_deref()),
      p
    );
  }

  out.push_str(&map_consts);
  if !map_consts.is_empty() {
    out.push('\n');
  }
  out.push_str("pub static FIELDS: &[Field] = &[\n");
  out.push_str(&fields);
  out.push_str("];\n");
  out
}

/// Render EVERY `XMP`-group `<table>` as one committed file: per-namespace
/// `pub static GEN_<NS>: &[Field]`, the value-map consts (slices + phf), and a
/// `pub static GEN_NS_TABLES: &[(&str, &[Field])]` index keyed by the routing
/// namespace (the `XMP-<ns>` family-1 group with `XMP-` stripped). The hand
/// table wires its 7 namespaces' `GEN_<NS>` slices as the fallback layer; the
/// rest are surfaced as generated-only namespace tables (see `tables.rs`).
///
/// Tables are emitted in namespace-sorted order for a deterministic,
/// review-stable file; `--check` (Task 8) diffs byte-for-byte against this.
pub fn emit_xmp_tables(tables: &[TableModel]) -> String {
  // Sort by routing namespace so the output is deterministic regardless of
  // `-listx` document order.
  let mut sorted: Vec<&TableModel> = tables.iter().collect();
  sorted.sort_by(|a, b| ns_of(a).cmp(ns_of(b)));

  let mut out = String::new();
  out.push_str("// generated by xtask gen-tables from exiftool -listx 13.59 — DO NOT EDIT\n");
  out.push_str("// (regenerate with `cargo xtask gen-tables --module XMP \\\n");
  out.push_str("//    --out src/formats/xmp/tables_generated.rs`)\n");
  out.push_str("//\n");
  let _ = writeln!(
    out,
    "// Full XMP namespace surface: {} namespaces, {} tags. The hand-written\n\
     // `tables.rs` shadows any tag it already defines (hand wins on key\n\
     // collision); these `GEN_*` slices are the additive fallback + the\n\
     // sole source for namespaces the hand table does not list.",
    sorted.len(),
    sorted.iter().map(|t| t.tags.len()).sum::<usize>(),
  );
  out.push_str("#![allow(clippy::unreadable_literal)]\n\n");
  out.push_str("use super::tables::{Field, PrintConv as P, Writable as W};\n\n");

  let mut map_consts = String::new();
  let mut all_fields = String::new();
  let mut index = String::new();

  for model in &sorted {
    let ns = ns_of(model);
    let ident = ns_ident(ns);

    // Per-namespace fields (each appends its map consts as encountered).
    let mut fields = String::new();
    for t in &model.tags {
      let p = field_print_conv_ns(ns, t, &mut map_consts);
      let _ = writeln!(
        fields,
        "  Field::make(\"{}\", {}, {}, {}),",
        t.id,
        name_arg(t),
        writable_variant(t.ty.as_deref()),
        p
      );
    }

    let _ = writeln!(
      all_fields,
      "/// `{}` (g1 `{}`) — {} tags.",
      model.name,
      model.groups.1,
      model.tags.len()
    );
    let _ = writeln!(all_fields, "pub static {ident}: &[Field] = &[");
    all_fields.push_str(&fields);
    all_fields.push_str("];\n\n");

    let _ = writeln!(index, "  (\"{ns}\", {ident}),");
  }

  out.push_str(&map_consts);
  if !map_consts.is_empty() {
    out.push('\n');
  }
  out.push_str(&all_fields);
  out.push_str(
    "/// Every generated XMP namespace table, keyed by its routing namespace\n\
     /// (the `XMP-<ns>` family-1 group with `XMP-` stripped). Consulted by\n\
     /// `tables::lookup_ns_table` AFTER the hand-written tables.\n",
  );
  out.push_str("pub static GEN_NS_TABLES: &[(&str, &[Field])] = &[\n");
  out.push_str(&index);
  out.push_str("];\n");
  out
}

/// The routing namespace of a table — its `g1` with the `XMP-` prefix stripped
/// (e.g. `XMP-tiff` → `tiff`, `XMP-mwg-rs` → `mwg-rs`). Falls back to the raw
/// `g1` if it does not start with `XMP-`.
fn ns_of(model: &TableModel) -> &str {
  model
    .groups
    .1
    .strip_prefix("XMP-")
    .unwrap_or(&model.groups.1)
}

/// Sanitize a routing namespace to a `SCREAMING_SNAKE` Rust identifier, prefixed
/// `GEN_` (e.g. `mwg-rs` → `GEN_MWG_RS`, `drone-dji` → `GEN_DRONE_DJI`).
fn ns_ident(ns: &str) -> String {
  format!("GEN_{}", sanitize_upper(ns))
}

/// `<NS>_<TAGID>` const name for a value-map, sanitized + uppercased and
/// namespace-prefixed for global uniqueness (e.g. (`tiff`, `Compression`) →
/// `TIFF_COMPRESSION`).
fn map_const_name(ns: &str, tag: &str) -> String {
  format!("{}_{}", sanitize_upper(ns), sanitize_upper(tag))
}

/// Uppercase + replace every non-alphanumeric character with `_` so the result
/// is a valid Rust identifier fragment.
fn sanitize_upper(s: &str) -> String {
  s.chars()
    .map(|c| {
      if c.is_ascii_alphanumeric() {
        c.to_ascii_uppercase()
      } else {
        '_'
      }
    })
    .collect()
}

/// The `Some("Name")` / `None` `name` argument — `None` when the emitted name
/// equals the property key (no ExifTool `Name` remap), else the remapped name.
fn name_arg(t: &crate::listx::TagModel) -> String {
  if t.name == t.id {
    "None".to_string()
  } else {
    format!("Some({:?})", t.name)
  }
}

/// The single-table-path PrintConv expression (uses the table NAME's leaf for
/// the const prefix, matching the original Task-5 emitter).
fn field_print_conv(
  table_name: &str,
  t: &crate::listx::TagModel,
  map_consts: &mut String,
) -> String {
  let leaf = table_name.rsplit("::").next().unwrap_or("XMP");
  print_conv_expr(leaf, t, map_consts)
}

/// The all-namespaces-path PrintConv expression (uses the routing namespace for
/// the const prefix so consts are unique + traceable to their `XMP-<ns>` group).
fn field_print_conv_ns(ns: &str, t: &crate::listx::TagModel, map_consts: &mut String) -> String {
  print_conv_expr(ns, t, map_consts)
}

/// Resolve a tag to its `P::*` expression, appending any value-map const to
/// `map_consts`. `prefix` is the const-name namespace.
fn print_conv_expr(prefix: &str, t: &crate::listx::TagModel, map_consts: &mut String) -> String {
  match resolve_printconv(&t.name, &t.values, t.writable.as_deref()) {
    Conv::Identity => "P::Identity".to_string(),
    Conv::Unported(src) => format!("P::Unported({:?})", format!("{src}:{}", t.name)),
    conv @ (Conv::IntMap | Conv::StrMap) => {
      let values = t.values.as_deref().unwrap_or_default();
      let cname = map_const_name(prefix, &t.id);
      if values.len() > PHF_THRESHOLD {
        emit_phf_map_const(map_consts, &cname, &conv, values);
        format!("P::MapPhf(&{cname})")
      } else {
        emit_slice_map_const(map_consts, &cname, &conv, values);
        let variant = if matches!(conv, Conv::IntMap) {
          "IntMap"
        } else {
          "StrMap"
        };
        format!("P::{variant}({cname})")
      }
    }
  }
}

/// Render `model` as committed Rust source in the **generic** `src/tagtable.rs`
/// vocabulary — `TagDef::new(name, group1, ValueConv::*, PrintConv::*)` entries
/// plus their value-map consts — for the audio/container tag-table formats
/// (FLAC, OGG, AIFF, APE, MPC, DSF, AAC, DV, …). This is the second emit target
/// alongside the XMP [`emit_table`] / [`emit_xmp_tables`] `Field` vocabulary.
///
/// Shape (mirrors the hand tables in `src/formats/*.rs`): one
/// `static <NAME>: TagDef = …;` per tag, the `&[(&str, PrintValue)]` map consts
/// for value-mapped tags, and a `pub fn get(id: TagId) -> Option<&'static
/// TagDef>` whose `match` is keyed by the faithful [`crate::tagtable::TagId`]
/// (numeric `<tag id>` → `TagId::Int`, string id → `TagId::Str`). The caller
/// wraps it in a `TagTable::new(group0, get)`.
///
/// PrintConv mapping into the generic vocabulary (which — unlike XMP's
/// `P::IntMap`/`P::StrMap` — has NO map variants, only a Perl-hash
/// `PrintConv::Hash(PrintConvHash::direct(&[(&str, PrintValue)]))`):
/// * a `<values>` map → `PrintConv::Hash(PrintConvHash::direct(&<CONST>))`,
///   keyed by the STRINGIFIED `$val` exactly as ExifTool indexes `$$conv{$val}`
///   (ExifTool.pm:3605); an all-numeric-label map emits `PrintValue::I64`,
///   otherwise `PrintValue::Str` (the AAC `Channels` `2 => 2` numeric-scalar
///   shape vs the `ProfileType` `0 => 'Main'` string shape);
/// * no map, plain tag → `PrintConv::None`;
/// * a code-valued conv flagged by [`crate::conv_registry`] →
///   `PrintConv::Unported("<group0>:<Tag>")` (faithful raw passthrough).
///
/// `group0` (the family-0 group) and `group1` (per-tag family-1) come from the
/// `<table>` `g0` / `g1`; `-listx` carries no value-map for a code-valued
/// `ValueConv`, so ValueConv is always `ValueConv::None` here (a format that
/// needs a real ValueConv hand-writes it — same incremental rule as the XMP
/// `HANDPORTED` escape hatch).
pub fn emit_tagdef_table(model: &TableModel) -> String {
  let group0 = if model.groups.0.is_empty() {
    model.name.split("::").next().unwrap_or("File")
  } else {
    &model.groups.0
  };
  let group1 = if model.groups.1.is_empty() {
    group0
  } else {
    &model.groups.1
  };
  let leaf = model.name.rsplit("::").next().unwrap_or("TABLE");

  let mut out = String::new();
  out.push_str("// generated by xtask gen-tables from exiftool -listx 13.59 — DO NOT EDIT\n");
  let _ = writeln!(
    out,
    "// (regenerate with `cargo xtask gen-tables --kind tagdef --module {} \\\n\
     //    --out <path>`)",
    module_arg(model)
  );
  out.push_str("// source table: ");
  out.push_str(&model.name);
  out.push('\n');

  let mut map_consts = String::new();
  let mut statics = String::new();
  let mut arms = String::new();
  // `PrintConvHash`/`PrintValue` are only referenced when at least one tag has
  // a `<values>` map; emitting them unconditionally would be an `unused_imports`
  // warning (the `-Dwarnings` gate) for a map-less table (e.g. FLAC StreamInfo).
  let mut needs_hash_imports = false;
  // The `static <IDENT>` name derives from the tag NAME ([`static_ident`]), but
  // ExifTool aliases distinct frame ids to one name (e.g. ID3v2.2 `GP1` AND
  // `TT1` → `Grouping`; v2.4 `TSOT`/`XSOT` → `TitleSortOrder`). Those aliases
  // share an IDENTICAL `TagDef` body, so emit each `static` ONCE (keyed by its
  // ident) and point every alias's `get` arm at the single static — a faithful,
  // lossless collapse (the alternative, duplicate `static` decls, is an `E0428`
  // "defined multiple times"). If two ids ever produced the same ident with a
  // DIFFERENT body, the generator would be unsound, so assert that never holds.
  let mut emitted: std::collections::HashMap<String, String> = std::collections::HashMap::new();

  for t in &model.tags {
    let sname = static_ident(leaf, &t.name);
    let pc = tagdef_print_conv(group0, leaf, t, &mut map_consts, &mut needs_hash_imports);
    let decl = format!(
      "static {sname}: TagDef =\n  TagDef::new({:?}, {:?}, ValueConv::None, {});\n",
      t.name, group1, pc
    );
    match emitted.get(&sname) {
      None => {
        statics.push_str(&decl);
        emitted.insert(sname.clone(), decl);
      }
      Some(prev) => assert_eq!(
        prev, &decl,
        "static ident `{sname}` would be emitted with two DIFFERENT bodies \
         (aliased ids must share an identical TagDef); generator is unsound"
      ),
    }
    let key = tagid_key(&t.id);
    let _ = writeln!(arms, "    {key} => Some(&{sname}),");
  }

  // Adaptive import line: `PrintConvHash`/`PrintValue` only when a value-map
  // is present, so a map-less table is `unused_imports`-clean under `-Dwarnings`.
  out.push('\n');
  if needs_hash_imports {
    out.push_str(
      "use crate::tagtable::{PrintConv, PrintConvHash, PrintValue, TagDef, TagId, ValueConv};\n\n",
    );
  } else {
    out.push_str("use crate::tagtable::{PrintConv, TagDef, TagId, ValueConv};\n\n");
  }

  out.push_str(&map_consts);
  if !map_consts.is_empty() {
    out.push('\n');
  }
  out.push_str(&statics);
  out.push('\n');
  let _ = writeln!(
    out,
    "/// `{}` (g0 `{}`, g1 `{}`) — {} tags.\n\
     pub fn get(id: TagId) -> Option<&'static TagDef> {{\n  \
       match id {{\n{}    _ => None,\n  }}\n}}",
    model.name,
    group0,
    group1,
    model.tags.len(),
    arms,
  );
  out
}

/// The `TagId` match key for a tag id: a numeric id → `TagId::Int(<n>)`, any
/// other (string) id → `TagId::Str("<id>")` — faithful to how ExifTool indexes
/// its tag-table hash (numeric vs string Perl keys, cf. AAC `'Bit016-017'`).
fn tagid_key(id: &str) -> String {
  if let Ok(n) = id.parse::<i64>() {
    format!("TagId::Int({n})")
  } else {
    format!("TagId::Str({id:?})")
  }
}

/// The `static <IDENT>: TagDef` name for a tag — `<LEAF>_<NAME>`, sanitized +
/// uppercased from the emitted tag NAME (readable, vs an opaque numeric id;
/// e.g. (`StreamInfo`, `SampleRate`) → `STREAMINFO_SAMPLERATE`,
/// (`Main`, `ChannelCount`) → `MAIN_CHANNELCOUNT`). The `get` match still keys
/// by the faithful [`tagid_key`], so an id↔name remap is preserved.
fn static_ident(leaf: &str, name: &str) -> String {
  format!("{}_{}", sanitize_upper(leaf), sanitize_upper(name))
}

/// Resolve a tag to its generic `PrintConv::*` expression, appending any
/// `&[(&str, PrintValue)]` map const to `map_consts`. `group0` is the
/// provenance namespace for an `Unported` marker; `leaf` prefixes the const.
fn tagdef_print_conv(
  group0: &str,
  leaf: &str,
  t: &crate::listx::TagModel,
  map_consts: &mut String,
  needs_hash_imports: &mut bool,
) -> String {
  match resolve_printconv(&t.name, &t.values, t.writable.as_deref()) {
    Conv::Identity => "PrintConv::None".to_string(),
    Conv::Unported(_) => format!("PrintConv::Unported({:?})", format!("{group0}:{}", t.name)),
    conv @ (Conv::IntMap | Conv::StrMap) => {
      *needs_hash_imports = true;
      let values = t.values.as_deref().unwrap_or_default();
      // `_MAP` suffix keeps the value-map const in a distinct namespace from
      // the tag's `static <LEAF>_<NAME>: TagDef` (which shares the base ident),
      // so the two never collide (e.g. DSF `FormatID` → `MAIN_FORMATID_MAP`
      // const + `MAIN_FORMATID` TagDef).
      let cname = format!("{}_MAP", map_const_name(leaf, &t.name));
      emit_print_hash_const(map_consts, &cname, &conv, values);
      format!("PrintConv::Hash(PrintConvHash::direct(&{cname}))")
    }
  }
}

/// Emit a generic-vocabulary value-map const: `&[(&str, PrintValue)]` keyed by
/// the STRINGIFIED `$val` (ExifTool.pm:3605). Each LABEL's `PrintValue` variant
/// is decided PER ENTRY, faithfully mirroring ExifTool's Perl scalars + their
/// `-j` JSON rendering: a label that is a bare integer (e.g. AAC `Channels`
/// `1 => 1`) → `PrintValue::I64` (emitted as a JSON number); anything else
/// (e.g. `6 => '5+1'`, `0 => 'Main'`) → `PrintValue::Str`. ExifTool stores `1`
/// and `'5+1'` in the SAME conv hash with DIFFERENT scalar types, so an
/// all-or-nothing rule would mis-type the numeric entries; the hand tables
/// (`AAC::Main` `Channels`) carry exactly this mix. The KEY is always the raw
/// scalar string so the runtime `$$conv{$val}` exact-string lookup is faithful.
fn emit_print_hash_const(buf: &mut String, name: &str, _conv: &Conv, values: &[(String, String)]) {
  let entries: Vec<String> = values
    .iter()
    .map(|(k, v)| {
      if v.parse::<i64>().is_ok() {
        format!("({k:?}, PrintValue::I64({v}))")
      } else {
        format!("({k:?}, PrintValue::Str({v:?}))")
      }
    })
    .collect();
  let _ = writeln!(
    buf,
    "static {name}: &[(&str, PrintValue)] = &[{}];",
    entries.join(", ")
  );
}

/// Map a `-listx` `<tag type=…>` to its [`super::tables::Writable`] variant.
/// An ABSENT type → `W::None` (no explicit `Writable`; XMPAutoConv applies);
/// the `?` ("integer?"-style undeclared) and `struct` (a container, never a
/// scalar leaf) types — and any unrecognized type — map to `W::Str`, which
/// disables XMPAutoConv exactly like the hand table's `{}`→`W::Str` mapping.
fn writable_variant(ty: Option<&str>) -> &'static str {
  match ty {
    None => "W::None",
    Some("integer") => "W::Integer",
    Some("rational") => "W::Rational",
    Some("real") => "W::Real",
    Some("boolean") => "W::Boolean",
    Some("date") => "W::Date",
    Some("lang-alt") => "W::LangAlt",
    Some("string") => "W::Str",
    Some(_) => "W::Str",
  }
}

/// Emit a sorted-slice PrintConv map const (`&[(i64,&str)]` / `&[(&str,&str)]`).
fn emit_slice_map_const(buf: &mut String, name: &str, conv: &Conv, values: &[(String, String)]) {
  match conv {
    Conv::IntMap => {
      let entries: Vec<String> = values
        .iter()
        .map(|(k, v)| format!("({k}, {v:?})"))
        .collect();
      let _ = writeln!(
        buf,
        "const {name}: &[(i64, &str)] = &[{}];",
        entries.join(", ")
      );
    }
    Conv::StrMap => {
      let entries: Vec<String> = values
        .iter()
        .map(|(k, v)| format!("({k:?}, {v:?})"))
        .collect();
      let _ = writeln!(
        buf,
        "const {name}: &[(&str, &str)] = &[{}];",
        entries.join(", ")
      );
    }
    Conv::Identity | Conv::Unported(_) => {}
  }
}

/// Emit a `phf::Map<&'static str, &'static str>` const for a LARGE value-map,
/// keyed by the RAW scalar string (an `i64` key is stringified to its decimal
/// form) so the lookup is the faithful `$$conv{$val}` exact-string match. Built
/// by `phf_codegen` at generation time → a perfect-hash literal with no runtime
/// build cost.
fn emit_phf_map_const(buf: &mut String, name: &str, _conv: &Conv, values: &[(String, String)]) {
  let mut m = phf_codegen::Map::new();
  for (k, v) in values {
    // Both int- and string-keyed maps key by the raw scalar STRING (an int key
    // is already its decimal text in `-listx`), matching the slice path's
    // `k.to_string() == numeric` lookup semantics.
    m.entry(k.as_str(), &format!("{v:?}"));
  }
  let _ = writeln!(
    buf,
    "static {name}: phf::Map<&'static str, &'static str> = {};",
    m.build()
  );
}

