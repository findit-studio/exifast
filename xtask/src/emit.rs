/// Which EXIF tag-table vocabulary to emit: the IFD `Exif::Main` ([`ExifTag`] /
/// [`crate::exif_conv::resolve_exif`]) or the GPS sub-IFD `GPS::Main`
/// ([`GpsTag`] / [`crate::exif_conv::resolve_gps`]).
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum ExifKind {
  /// `%Image::ExifTool::Exif::Main` → `src/exif/tables.rs` `ExifTag` rows.
  Exif,
  /// `%Image::ExifTool::GPS::Main` → `src/exif/gps.rs` `GpsTag` rows.
  Gps,
}

/// Render `model` (`Exif::Main` or `GPS::Main`) as committed Rust source in the
/// EXIF module's hand `ExifTag` / `GpsTag` vocabulary — the `--kind exif` emit
/// target — RESTRICTED to `allow_ids` (the generator's allowlist,
/// `exifast::exif::exif_main_tag_ids` / `gps_main_tag_ids`).
///
/// Step A was a BYTE-IDENTICAL shadow of the hand table (hand ids only); Step B
/// extends the `Exif::Main` allowlist with the binary-coverage-gap ids
/// (`exif_main_tag_ids` = hand ids + `EXIF_MAIN_GAP_IDS`) so the generated table
/// is a SUPERSET — every SHARED id resolves by [`crate::exif_conv`] to the SAME
/// `Conv` the hand table carries (a per-id differential parity test in the lib
/// is that gate), and the gap ids (absent from the hand table) emit through the
/// hand-first `lookup`'s fallback. The emitted file is a child module of
/// `src/exif/tables.rs` (resp. `gps.rs`), so the HANDPORTED `super::COMPRESSION`
/// / `super::GPS_MEASURE_MODE` const references resolve against the parent's
/// curated label slices and the shadow reuses them byte-for-byte. (`GPS::Main`
/// has no gap ids, so its shadow stays the Step-A byte-identical subset.)
///
/// Shape (mirrors [`emit_tagdef_table`]): one `static <IDENT>: ExifTag = …;`
/// per tag (keyed by the resolved NAME, deduped-by-ident with a soundness
/// assert), then a `pub fn lookup(id: u16) -> Option<&'static ExifTag>` whose
/// `match` is keyed by the on-disk id (rendered `0x%04x`). The hand `lookup`
/// consults its own table FIRST and falls back here; since `allow_ids` ⊆ hand,
/// that fallback is inert (the shadow can only ever AGREE).
///
/// A `-listx` id may appear under several `Condition` branches (e.g. 0x0111
/// `StripOffsets` / `JpgFromRawStart` / `PreviewImageStart`); the FIRST
/// document-order entry is taken. Every such duplicated id in the hand set is a
/// name-only HANDPORTED override with NO `<values>` map, so the branch choice
/// affects neither the (overridden) name nor the (`None`) conv.
pub fn emit_exif_table(model: &TableModel, kind: ExifKind, allow_ids: &[u16]) -> String {
  let (ty, lookup_ty, regen_module) = match kind {
    ExifKind::Exif => ("ExifTag", "ExifTag", "Exif::Main"),
    ExifKind::Gps => ("GpsTag", "GpsTag", "GPS::Main"),
  };

  let mut out = String::new();
  out.push_str("// generated by xtask gen-tables from exiftool -listx 13.59 — DO NOT EDIT\n");
  let _ = writeln!(
    out,
    "// (regenerate with `cargo xtask gen-tables --kind exif --module {regen_module} \\\n\
     //    --out <path>`)"
  );
  out.push_str("// source table: ");
  out.push_str(&model.name);
  out.push('\n');
  out.push_str(
    "//\n\
     // Generated shadow of the hand table — the ported camera-relevant hand id\n\
     // set PLUS the binary-EXIF coverage-gap ids (Step B). Every SHARED id\n\
     // resolves to the SAME conv the hand table carries; the gap ids are NOT in\n\
     // the hand table, so the hand-first `lookup` falls through here and they\n\
     // emit. A child module of the hand table (`super::*` const refs resolve\n\
     // there); the hand table stays a strict SUBSET of the generated table.\n",
  );

  // `Conv` is always referenced; `GpsConv` only in the GPS file. Imports come
  // from the parent module (`super`), where `ExifTag`/`Conv` (resp.
  // `GpsTag`/`GpsConv`) live.
  out.push('\n');
  match kind {
    ExifKind::Exif => out.push_str("use super::{Conv, ExifTag};\n\n"),
    ExifKind::Gps => out.push_str("use super::{Conv, GpsConv, GpsTag};\n\n"),
  }

  let allow: std::collections::HashSet<u16> = allow_ids.iter().copied().collect();
  let mut statics = String::new();
  let mut arms = String::new();
  // Dedup the emitted `static`s by ident (the resolved NAME is unique across
  // the hand set, but assert it: two ids that produced the same ident with a
  // DIFFERENT body would be an unsound collapse — cf. `emit_tagdef_table`).
  let mut emitted: std::collections::HashMap<String, String> = std::collections::HashMap::new();
  // Take the FIRST `-listx` entry per id (document order); a duplicated id's
  // later `Condition` branches are name-overridden + map-less, so the choice is
  // immaterial. `seen` also guards against emitting a `match` arm twice.
  let mut seen: std::collections::HashSet<u16> = std::collections::HashSet::new();

  for t in &model.tags {
    let Ok(id) = parse_tag_id(&t.id) else {
      continue;
    };
    if !allow.contains(&id) || !seen.insert(id) {
      continue;
    }
    let resolved = match kind {
      ExifKind::Exif => crate::exif_conv::resolve_exif(id, t),
      ExifKind::Gps => crate::exif_conv::resolve_gps(id, t),
    };
    let sname = exif_static_ident(&resolved.name);
    let decl = format!(
      "static {sname}: {ty} = {ty} {{ id: {id:#06x}, name: {:?}, conv: {} }};\n",
      resolved.name, resolved.conv_expr
    );
    match emitted.get(&sname) {
      None => {
        statics.push_str(&decl);
        emitted.insert(sname.clone(), decl);
      }
      Some(prev) => assert_eq!(
        prev, &decl,
        "static ident `{sname}` would be emitted with two DIFFERENT bodies; \
         generator is unsound"
      ),
    }
    let _ = writeln!(arms, "    {id:#06x} => Some(&{sname}),");
  }

  out.push_str(&statics);
  out.push('\n');
  let _ = writeln!(
    out,
    "/// `{}` — the generated shadow ({} ids: the ported hand subset + the\n\
     /// binary-coverage-gap ids). Consulted by the hand `lookup` AFTER its own\n\
     /// table: a SHARED id always AGREES with the hand entry, and a gap id (NOT\n\
     /// in the hand table) is the only one this fallback actually returns.\n\
     pub fn lookup(id: u16) -> Option<&'static {lookup_ty}> {{\n  \
       match id {{\n{}    _ => None,\n  }}\n}}",
    model.name,
    seen.len(),
    arms,
  );
  out
}

/// The `static <IDENT>: ExifTag` name for an EXIF/GPS tag — `EXIF_<NAME>`,
/// sanitized + uppercased from the resolved tag NAME (readable; e.g.
/// `ExposureTime` → `EXIF_EXPOSURETIME`, `GPSLatitude` → `EXIF_GPSLATITUDE`).
/// The `lookup` match keys by the on-disk id, so the ident is cosmetic; a
/// single `EXIF_` prefix keeps both files' idents in one namespace.
fn exif_static_ident(name: &str) -> String {
  format!("EXIF_{}", sanitize_upper(name))
}

/// Parse a `-listx` `<tag id>` to its numeric `u16`. EXIF/GPS ids are always
/// numeric (decimal in `-listx`); a non-numeric id (none in `Exif::Main` /
/// `GPS::Main`'s leaf set) is rejected so it is skipped by the allowlist filter.
fn parse_tag_id(id: &str) -> Result<u16, std::num::ParseIntError> {
  id.parse::<u16>()
}

