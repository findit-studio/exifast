/// One verified-conv-less candidate atom for the QuickTime supplementary map:
/// its emitted tag NAME (the allowlist key, cross-referenced against
/// `QuickTime.pm`). The id/bytes come from the matching `-listx` row.
pub struct QuickTimeCandidate {
  /// The emitted tag NAME (e.g. `"GoProType"`), used as the allowlist key.
  pub name: &'static str,
}

/// Render the SP2 supplementary QuickTime map — the conv-less, non-priority
/// plain-string `udta` / `Keys` camera atoms NOT already special-cased by the
/// typed [`crate::metadata::QuickTimeUserData`] / [`QuickTimeKeys`] walker — as
/// a checked-in `src/formats/quicktime_generated.rs`.
///
/// This is the FOURTH emit target (alongside XMP `field`, generic `tagdef`,
/// EXIF `exif`). Unlike them it does NOT emit a `TagDef`/`Field` tag-table: SP2
/// uses the DOMAIN-STRUCT archetype (typed `QuickTimeUserData`/`QuickTimeKeys`
/// + `PriorityValue` Avoid/priority dedup + per-atom value decode), which the
/// generic tag-table archetypes cannot express. So the only thing safe to
/// generate is a flat `4cc → Name` (resp. `key-string → Name`) lookup for the
/// atoms that are GENUINELY conv-less — plain `string`/text with NO
/// RawConv/ValueConv/PrintConv-sub and NO `Avoid`/`Priority` — which the walker
/// consults to emit a tag by Name (the value via SP2's existing text decode).
///
/// ## The `-listx` correctness guard (the FLAC/EXIF lesson)
///
/// `exiftool -listx` carries NO `ValueConv`/`RawConv` and NO `Avoid`/`Priority`
/// flag — so a `ValueConv`-bearing atom like `CAME` (`unpack("H*",$val)`) or
/// `MUID` shows up as an ordinary `type='string'` row, indistinguishable from a
/// truly conv-less one. A naive `-listx`→map gen would therefore emit a LOSSY
/// `CAME → SerialNumberHash` entry that skips the hex `ValueConv`. To stay
/// faithful this emitter is allowlist-driven: it emits an entry ONLY for a
/// candidate whose NAME is in `userdata_allow` / `keys_allow` — each of which
/// was hand-verified conv-less against `QuickTime.pm`. Every candidate NAME the
/// caller passed in `unported` (the ones found to carry a conv/priority in the
/// Perl source — `CAME`/`MUID`/`AndroidCaptureFPS`/`AndroidTimeZone`) is emitted
/// into an `UNPORTED` documentation slice instead (and stays HAND-ported in the
/// walker), never a lossy lookup entry. A `-listx` row whose `<tag id>` carries
/// an ExifTool `\xNN`/`\x00` escape (the `©`-prefixed and `FOV\0` atoms) is
/// decoded back to its raw 4-cc bytes by [`decode_listx_id_bytes`].
pub fn emit_quicktime_map(
  userdata: &TableModel,
  keys: &TableModel,
  userdata_allow: &[QuickTimeCandidate],
  keys_allow: &[QuickTimeCandidate],
  unported: &[&str],
) -> String {
  let mut out = String::new();
  out.push_str("// generated by xtask gen-tables from exiftool -listx 13.59 — DO NOT EDIT\n");
  out.push_str("// (regenerate with `cargo xtask gen-tables --kind quicktime \\\n");
  out.push_str("//    --module QuickTime::UserData \\\n");
  out.push_str("//    --out src/formats/quicktime_generated.rs`)\n");
  out.push_str("//\n");
  out.push_str(
    "// SP2 supplementary conv-less camera-atom map: the plain-string\n\
     // `%QuickTime::UserData` (4cc keys) and `%QuickTime::Keys` (string keys)\n\
     // atoms that carry NO RawConv/ValueConv/PrintConv-sub and NO Avoid/Priority\n\
     // in QuickTime.pm — each hand-verified against the Perl source (`-listx`\n\
     // alone cannot prove conv-less, cf. CAME/MUID which look plain but carry a\n\
     // hex ValueConv → kept in UNPORTED + hand-ported). The walker consults\n\
     // these to emit a `QuickTime:UserData` / `QuickTime:Keys` tag by Name.\n",
  );
  // The file is `include!`d into `mod quicktime::quicktime_generated`, so
  // `super` is the parent `quicktime` module where `ConvlessAtom` lives.
  out.push_str("\nuse super::ConvlessAtom;\n\n");

  // ── UserData: 4cc-byte keys ──────────────────────────────────────────────
  let ud_entries = quicktime_table_entries(userdata, userdata_allow);
  let _ = writeln!(
    out,
    "/// `%QuickTime::UserData` (g0 `{}`, g1 `{}`) conv-less plain-string atoms,\n\
     /// keyed by the raw 4-character-code bytes (the `©`-prefixed and `FOV\\0`\n\
     /// atoms carry their non-ASCII bytes verbatim). {} atoms.",
    group0_of(userdata),
    group1_of(userdata),
    ud_entries.len(),
  );
  out.push_str("pub static QUICKTIME_USERDATA_CONVLESS: &[ConvlessAtom<&[u8]>] = &[\n");
  for (key_bytes, name) in &ud_entries {
    let _ = writeln!(
      out,
      "  ConvlessAtom::new(&{}, {:?}),",
      byte_array_literal(key_bytes),
      name
    );
  }
  out.push_str("];\n\n");

  // ── Keys: string keys ────────────────────────────────────────────────────
  let k_entries = quicktime_table_entries(keys, keys_allow);
  let _ = writeln!(
    out,
    "/// `%QuickTime::Keys` (g0 `{}`, g1 `{}`) conv-less plain-string atoms,\n\
     /// keyed by the key string (after the `com.apple.quicktime.` strip the\n\
     /// walker applies). {} atoms.",
    group0_of(keys),
    group1_of(keys),
    k_entries.len(),
  );
  out.push_str("pub static QUICKTIME_KEYS_CONVLESS: &[ConvlessAtom<&str>] = &[\n");
  for (key, name) in &k_entries {
    let key_str = String::from_utf8_lossy(key);
    let _ = writeln!(out, "  ConvlessAtom::new({:?}, {:?}),", key_str, name);
  }
  out.push_str("];\n\n");

  // ── Unported (conv/priority-bearing) candidates — documentation only ─────
  out.push_str(
    "/// Candidate camera atoms that LOOK conv-less in `-listx` but carry a\n\
     /// ValueConv / float decode / group in `QuickTime.pm`, so they are NOT in\n\
     /// the maps above — they stay HAND-ported in the typed walker (faithful to\n\
     /// the conv). Listed here for audit + drift coverage.\n",
  );
  out.push_str("pub static UNPORTED: &[&str] = &[\n");
  for name in unported {
    let _ = writeln!(out, "  {name:?},");
  }
  out.push_str("];\n");
  out
}

/// Collect the `(4cc-bytes, Name)` entries of `model` that are in `allow`, in
/// allowlist order (deterministic, review-stable). A candidate NAME with no
/// matching `-listx` row is an ERROR (the allowlist drifted from the table).
fn quicktime_table_entries(
  model: &TableModel,
  allow: &[QuickTimeCandidate],
) -> Vec<(Vec<u8>, String)> {
  allow
    .iter()
    .map(|cand| {
      let tag = model
        .tags
        .iter()
        .find(|t| t.name == cand.name)
        .unwrap_or_else(|| {
          panic!(
            "QuickTime codegen allowlist NAME `{}` not found in `{}` -listx table; \
             the allowlist drifted from the bundled ExifTool",
            cand.name, model.name
          )
        });
      (decode_listx_id_bytes(&tag.id), tag.name.clone())
    })
    .collect()
}

/// Decode an `exiftool -listx` `<tag id>` back to its raw on-disk bytes.
/// ExifTool escapes a non-printable / non-ASCII id byte as the literal text
/// `\xNN` (lower-case hex) — e.g. `\xa9gpt` is the 4 bytes `A9 67 70 74`, and
/// `FOV\x00` is `46 4F 56 00`. An un-escaped ASCII id (`GoPr`) passes through
/// as its UTF-8 bytes. A literal backslash in an id would be `\x5c` (none of
/// the candidate atoms contain one), so a bare `\` not followed by `xNN` is
/// kept verbatim.
fn decode_listx_id_bytes(id: &str) -> Vec<u8> {
  let bytes = id.as_bytes();
  let mut out = Vec::with_capacity(bytes.len());
  let mut i = 0;
  while i < bytes.len() {
    let escape = (bytes[i] == b'\\' && bytes.get(i + 1) == Some(&b'x'))
      .then(|| {
        let hi = bytes.get(i + 2).and_then(|b| (*b as char).to_digit(16))?;
        let lo = bytes.get(i + 3).and_then(|b| (*b as char).to_digit(16))?;
        Some((hi * 16 + lo) as u8)
      })
      .flatten();
    match escape {
      Some(b) => {
        out.push(b);
        i += 4;
      }
      None => {
        out.push(bytes[i]);
        i += 1;
      }
    }
  }
  out
}

/// A `[0xA9, 0x67, 0x70, 0x74]` Rust byte-array literal for a 4-cc key.
fn byte_array_literal(bytes: &[u8]) -> String {
  let parts: Vec<String> = bytes.iter().map(|b| format!("{b:#04x}")).collect();
  format!("[{}]", parts.join(", "))
}

/// The family-0 group of a table — its `g0` when set, else the `Mod::` prefix.
fn group0_of(model: &TableModel) -> &str {
  if model.groups.0.is_empty() {
    model.name.split("::").next().unwrap_or("QuickTime")
  } else {
    &model.groups.0
  }
}

/// The family-1 group of a table — its `g1` when set, else [`group0_of`].
fn group1_of(model: &TableModel) -> &str {
  if model.groups.1.is_empty() {
    group0_of(model)
  } else {
    &model.groups.1
  }
}

/// The `--module` arg that regenerates a single table — the `g1` group when set
/// (e.g. `XMP-tiff`), else the raw table name.
fn module_arg(model: &TableModel) -> &str {
  if model.groups.1.is_empty() {
    &model.name
  } else {
    &model.groups.1
  }
}

