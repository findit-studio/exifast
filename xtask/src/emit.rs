//! Render [`TableModel`]s as committed Rust source in the XMP module's
//! `Field::make(...)` vocabulary (`Writable as W` / `PrintConv as P`).
//!
//! Two entry points:
//!
//! * [`emit_table`] ÔÇö one table Ôćĺ a `pub static FIELDS: &[Field]` (the
//!   single-namespace path the Task-5/6 unit tests pin).
//! * [`emit_xmp_tables`] ÔÇö EVERY `XMP`-group table Ôćĺ one committed file with a
//!   `pub static GEN_<NS>: &[Field]` per namespace + a `GEN_NS_TABLES` index
//!   (the full XMP surface, Task 7).
//!
//! The emitted file is a child module of `src/formats/xmp/` so it
//! `use super::tables::{Field, PrintConv as P, Writable as W}` and the
//! crate-local `Field::make` constructor is in scope. A PrintConv value-map is
//! a sorted `&[(i64, &str)]` / `&[(&str, &str)]` slice const when SMALL, or a
//! `phf::Map<&'static str, &'static str>` (built by `phf_codegen`) when it has
//! more than [`PHF_THRESHOLD`] entries ÔÇö both keyed by the RAW scalar string so
//! the `apply_print_conv` lookup is the faithful `$$conv{$val}` exact-string
//! match (ExifTool.pm:3604). A code-valued, map-less conv flagged by the
//! [`crate::conv_registry`] becomes `P::Unported("<module>:<Tag>")`.

use crate::conv_registry::{resolve_printconv, Conv};
use crate::listx::TableModel;
use std::fmt::Write as _;

/// Value-maps with MORE than this many entries are emitted as a
/// `phf::Map<&str, &str>` (O(1) lookup); smaller maps stay a sorted slice +
/// linear stringified scan (no perceptible cost, smaller codegen, and the
/// representation the hand-written tables already use). Across the full XMP
/// surface only 3 maps exceed 24 (PLUS `MediaSummaryCode` = 2143 entries,
/// `tiff:Compression` = 53, `exif:LightSource` = 32), so the threshold keeps
/// the generated file overwhelmingly slice-based while giving the one giant
/// string map an O(1) lookup. Resolves #46/FU-4 for the generated maps.
pub const PHF_THRESHOLD: usize = 24;

/// Render `model` as committed Rust source for the XMP module (single-table
/// path; the all-namespaces path is [`emit_xmp_tables`]).
pub fn emit_table(model: &TableModel) -> String {
  let mut out = String::new();
  out.push_str("// generated by xtask gen-tables from exiftool -listx 13.59 ÔÇö DO NOT EDIT\n");
  let _ = writeln!(
    out,
    "// (regenerate with `cargo xtask gen-tables --module {}`)",
    module_arg(model)
  );
  out.push_str("// source table: ");
  out.push_str(&model.name);
  out.push('\n');
  out.push_str("\nuse super::tables::{Field, PrintConv as P, Writable as W};\n\n");

  let mut map_consts = String::new();
  let mut fields = String::new();
  for t in &model.tags {
    let p = field_print_conv(&model.name, t, &mut map_consts);
    let _ = writeln!(
      fields,
      "  Field::make(\"{}\", {}, {}, {}),",
      t.id,
      name_arg(t),
      writable_variant(t.ty.as_deref()),
      p
    );
  }

  out.push_str(&map_consts);
  if !map_consts.is_empty() {
    out.push('\n');
  }
  out.push_str("pub static FIELDS: &[Field] = &[\n");
  out.push_str(&fields);
  out.push_str("];\n");
  out
}

/// Render EVERY `XMP`-group `<table>` as one committed file: per-namespace
/// `pub static GEN_<NS>: &[Field]`, the value-map consts (slices + phf), and a
/// `pub static GEN_NS_TABLES: &[(&str, &[Field])]` index keyed by the routing
/// namespace (the `XMP-<ns>` family-1 group with `XMP-` stripped). The hand
/// table wires its 7 namespaces' `GEN_<NS>` slices as the fallback layer; the
/// rest are surfaced as generated-only namespace tables (see `tables.rs`).
///
/// Tables are emitted in namespace-sorted order for a deterministic,
/// review-stable file; `--check` (Task 8) diffs byte-for-byte against this.
pub fn emit_xmp_tables(tables: &[TableModel]) -> String {
  // Sort by routing namespace so the output is deterministic regardless of
  // `-listx` document order.
  let mut sorted: Vec<&TableModel> = tables.iter().collect();
  sorted.sort_by(|a, b| ns_of(a).cmp(ns_of(b)));

  let mut out = String::new();
  out.push_str("// generated by xtask gen-tables from exiftool -listx 13.59 ÔÇö DO NOT EDIT\n");
  out.push_str("// (regenerate with `cargo xtask gen-tables --module XMP \\\n");
  out.push_str("//    --out src/formats/xmp/tables_generated.rs`)\n");
  out.push_str("//\n");
  let _ = writeln!(
    out,
    "// Full XMP namespace surface: {} namespaces, {} tags. The hand-written\n\
     // `tables.rs` shadows any tag it already defines (hand wins on key\n\
     // collision); these `GEN_*` slices are the additive fallback + the\n\
     // sole source for namespaces the hand table does not list.",
    sorted.len(),
    sorted.iter().map(|t| t.tags.len()).sum::<usize>(),
  );
  out.push_str("#![allow(clippy::unreadable_literal)]\n\n");
  out.push_str("use super::tables::{Field, PrintConv as P, Writable as W};\n\n");

  let mut map_consts = String::new();
  let mut all_fields = String::new();
  let mut index = String::new();

  for model in &sorted {
    let ns = ns_of(model);
    let ident = ns_ident(ns);

    // Per-namespace fields (each appends its map consts as encountered).
    let mut fields = String::new();
    for t in &model.tags {
      let p = field_print_conv_ns(ns, t, &mut map_consts);
      let _ = writeln!(
        fields,
        "  Field::make(\"{}\", {}, {}, {}),",
        t.id,
        name_arg(t),
        writable_variant(t.ty.as_deref()),
        p
      );
    }

    let _ = writeln!(
      all_fields,
      "/// `{}` (g1 `{}`) ÔÇö {} tags.",
      model.name,
      model.groups.1,
      model.tags.len()
    );
    let _ = writeln!(all_fields, "pub static {ident}: &[Field] = &[");
    all_fields.push_str(&fields);
    all_fields.push_str("];\n\n");

    let _ = writeln!(index, "  (\"{ns}\", {ident}),");
  }

  out.push_str(&map_consts);
  if !map_consts.is_empty() {
    out.push('\n');
  }
  out.push_str(&all_fields);
  out.push_str(
    "/// Every generated XMP namespace table, keyed by its routing namespace\n\
     /// (the `XMP-<ns>` family-1 group with `XMP-` stripped). Consulted by\n\
     /// `tables::lookup_ns_table` AFTER the hand-written tables.\n",
  );
  out.push_str("pub static GEN_NS_TABLES: &[(&str, &[Field])] = &[\n");
  out.push_str(&index);
  out.push_str("];\n");
  out
}

/// The routing namespace of a table ÔÇö its `g1` with the `XMP-` prefix stripped
/// (e.g. `XMP-tiff` Ôćĺ `tiff`, `XMP-mwg-rs` Ôćĺ `mwg-rs`). Falls back to the raw
/// `g1` if it does not start with `XMP-`.
fn ns_of(model: &TableModel) -> &str {
  model
    .groups
    .1
    .strip_prefix("XMP-")
    .unwrap_or(&model.groups.1)
}

/// Sanitize a routing namespace to a `SCREAMING_SNAKE` Rust identifier, prefixed
/// `GEN_` (e.g. `mwg-rs` Ôćĺ `GEN_MWG_RS`, `drone-dji` Ôćĺ `GEN_DRONE_DJI`).
fn ns_ident(ns: &str) -> String {
  format!("GEN_{}", sanitize_upper(ns))
}

/// `<NS>_<TAGID>` const name for a value-map, sanitized + uppercased and
/// namespace-prefixed for global uniqueness (e.g. (`tiff`, `Compression`) Ôćĺ
/// `TIFF_COMPRESSION`).
fn map_const_name(ns: &str, tag: &str) -> String {
  format!("{}_{}", sanitize_upper(ns), sanitize_upper(tag))
}

/// Uppercase + replace every non-alphanumeric character with `_` so the result
/// is a valid Rust identifier fragment.
fn sanitize_upper(s: &str) -> String {
  s.chars()
    .map(|c| {
      if c.is_ascii_alphanumeric() {
        c.to_ascii_uppercase()
      } else {
        '_'
      }
    })
    .collect()
}

/// The `Some("Name")` / `None` `name` argument ÔÇö `None` when the emitted name
/// equals the property key (no ExifTool `Name` remap), else the remapped name.
fn name_arg(t: &crate::listx::TagModel) -> String {
  if t.name == t.id {
    "None".to_string()
  } else {
    format!("Some({:?})", t.name)
  }
}

/// The single-table-path PrintConv expression (uses the table NAME's leaf for
/// the const prefix, matching the original Task-5 emitter).
fn field_print_conv(
  table_name: &str,
  t: &crate::listx::TagModel,
  map_consts: &mut String,
) -> String {
  let leaf = table_name.rsplit("::").next().unwrap_or("XMP");
  print_conv_expr(leaf, t, map_consts)
}

/// The all-namespaces-path PrintConv expression (uses the routing namespace for
/// the const prefix so consts are unique + traceable to their `XMP-<ns>` group).
fn field_print_conv_ns(ns: &str, t: &crate::listx::TagModel, map_consts: &mut String) -> String {
  print_conv_expr(ns, t, map_consts)
}

/// Resolve a tag to its `P::*` expression, appending any value-map const to
/// `map_consts`. `prefix` is the const-name namespace.
fn print_conv_expr(prefix: &str, t: &crate::listx::TagModel, map_consts: &mut String) -> String {
  match resolve_printconv(&t.name, &t.values, t.writable.as_deref()) {
    Conv::Identity => "P::Identity".to_string(),
    Conv::Unported(src) => format!("P::Unported({:?})", format!("{src}:{}", t.name)),
    conv @ (Conv::IntMap | Conv::StrMap) => {
      let values = t.values.as_deref().unwrap_or_default();
      let cname = map_const_name(prefix, &t.id);
      if values.len() > PHF_THRESHOLD {
        emit_phf_map_const(map_consts, &cname, &conv, values);
        format!("P::MapPhf(&{cname})")
      } else {
        emit_slice_map_const(map_consts, &cname, &conv, values);
        let variant = if matches!(conv, Conv::IntMap) {
          "IntMap"
        } else {
          "StrMap"
        };
        format!("P::{variant}({cname})")
      }
    }
  }
}

/// Map a `-listx` `<tag type=ÔÇŽ>` to its [`super::tables::Writable`] variant.
/// An ABSENT type Ôćĺ `W::None` (no explicit `Writable`; XMPAutoConv applies);
/// the `?` ("integer?"-style undeclared) and `struct` (a container, never a
/// scalar leaf) types ÔÇö and any unrecognized type ÔÇö map to `W::Str`, which
/// disables XMPAutoConv exactly like the hand table's `{}`Ôćĺ`W::Str` mapping.
fn writable_variant(ty: Option<&str>) -> &'static str {
  match ty {
    None => "W::None",
    Some("integer") => "W::Integer",
    Some("rational") => "W::Rational",
    Some("real") => "W::Real",
    Some("boolean") => "W::Boolean",
    Some("date") => "W::Date",
    Some("lang-alt") => "W::LangAlt",
    Some("string") => "W::Str",
    Some(_) => "W::Str",
  }
}

/// Emit a sorted-slice PrintConv map const (`&[(i64,&str)]` / `&[(&str,&str)]`).
fn emit_slice_map_const(buf: &mut String, name: &str, conv: &Conv, values: &[(String, String)]) {
  match conv {
    Conv::IntMap => {
      let entries: Vec<String> = values
        .iter()
        .map(|(k, v)| format!("({k}, {v:?})"))
        .collect();
      let _ = writeln!(
        buf,
        "const {name}: &[(i64, &str)] = &[{}];",
        entries.join(", ")
      );
    }
    Conv::StrMap => {
      let entries: Vec<String> = values
        .iter()
        .map(|(k, v)| format!("({k:?}, {v:?})"))
        .collect();
      let _ = writeln!(
        buf,
        "const {name}: &[(&str, &str)] = &[{}];",
        entries.join(", ")
      );
    }
    Conv::Identity | Conv::Unported(_) => {}
  }
}

/// Emit a `phf::Map<&'static str, &'static str>` const for a LARGE value-map,
/// keyed by the RAW scalar string (an `i64` key is stringified to its decimal
/// form) so the lookup is the faithful `$$conv{$val}` exact-string match. Built
/// by `phf_codegen` at generation time Ôćĺ a perfect-hash literal with no runtime
/// build cost.
fn emit_phf_map_const(buf: &mut String, name: &str, _conv: &Conv, values: &[(String, String)]) {
  let mut m = phf_codegen::Map::new();
  for (k, v) in values {
    // Both int- and string-keyed maps key by the raw scalar STRING (an int key
    // is already its decimal text in `-listx`), matching the slice path's
    // `k.to_string() == numeric` lookup semantics.
    m.entry(k.as_str(), &format!("{v:?}"));
  }
  let _ = writeln!(
    buf,
    "static {name}: phf::Map<&'static str, &'static str> = {};",
    m.build()
  );
}

/// The `--module` arg that regenerates a single table ÔÇö the `g1` group when set
/// (e.g. `XMP-tiff`), else the raw table name.
fn module_arg(model: &TableModel) -> &str {
  if model.groups.1.is_empty() {
    &model.name
  } else {
    &model.groups.1
  }
}

#[cfg(test)]
mod tests {
  use super::*;
  use crate::listx::{TableModel, TagModel};

  fn tag(id: &str, name: &str, ty: &str, values: Option<Vec<(&str, &str)>>) -> TagModel {
    TagModel {
      id: id.to_string(),
      name: name.to_string(),
      ty: Some(ty.to_string()),
      writable: Some("true".to_string()),
      values: values.map(|v| {
        v.into_iter()
          .map(|(k, val)| (k.to_string(), val.to_string()))
          .collect()
      }),
    }
  }

  #[test]
  fn emits_field_make_with_intmap_and_provenance() {
    let model = TableModel {
      name: "XMP::tiff".to_string(),
      groups: ("XMP".to_string(), "XMP-tiff".to_string()),
      tags: vec![
        tag(
          "Compression",
          "Compression",
          "integer",
          Some(vec![("1", "Uncompressed"), ("6", "JPEG (old-style)")]),
        ),
        // A `Name` remap (key != name) Ôćĺ emits `Some("ImageHeight")`.
        tag("ImageLength", "ImageHeight", "integer", None),
        // A plain string tag Ôćĺ `W::Str, P::Identity`.
        tag("Make", "Make", "string", None),
      ],
    };
    let src = emit_table(&model);

    // Provenance header.
    assert!(src.contains("// generated by xtask gen-tables from exiftool -listx"));
    // The IntMap field + its sorted-slice const, in the real vocabulary.
    assert!(
      src.contains(r#"Field::make("Compression", None, W::Integer, P::IntMap(TIFF_COMPRESSION))"#)
    );
    assert!(src.contains(
      r#"const TIFF_COMPRESSION: &[(i64, &str)] = &[(1, "Uncompressed"), (6, "JPEG (old-style)")];"#
    ));
    // Name remap Ôćĺ Some("ImageHeight").
    assert!(
      src.contains(r#"Field::make("ImageLength", Some("ImageHeight"), W::Integer, P::Identity)"#)
    );
    // Plain string tag.
    assert!(src.contains(r#"Field::make("Make", None, W::Str, P::Identity)"#));
    // The table is exposed as a `pub static FIELDS: &[Field]`.
    assert!(src.contains("pub static FIELDS: &[Field] = &["));
  }

  #[test]
  fn all_xmp_emits_per_namespace_statics_and_index() {
    let tables = vec![
      TableModel {
        name: "XMP::tiff".to_string(),
        groups: ("XMP".to_string(), "XMP-tiff".to_string()),
        tags: vec![tag("Make", "Make", "string", None)],
      },
      TableModel {
        name: "MWG::Regions".to_string(),
        groups: ("XMP".to_string(), "XMP-mwg-rs".to_string()),
        tags: vec![tag("Regions", "Regions", "struct", None)],
      },
    ];
    let src = emit_xmp_tables(&tables);
    // Per-namespace statics, hyphen-sanitized identifiers.
    assert!(src.contains("pub static GEN_TIFF: &[Field] = &["));
    assert!(src.contains("pub static GEN_MWG_RS: &[Field] = &["));
    // The index keys by the routing namespace (XMP- stripped).
    assert!(src.contains(r#"  ("tiff", GEN_TIFF),"#));
    assert!(src.contains(r#"  ("mwg-rs", GEN_MWG_RS),"#));
    assert!(src.contains("pub static GEN_NS_TABLES: &[(&str, &[Field])] = &["));
    // A `struct`-typed container maps to W::Str (no scalar auto-conv).
    assert!(src.contains(r#"Field::make("Regions", None, W::Str, P::Identity)"#));
  }

  #[test]
  fn large_map_emits_phf() {
    // > PHF_THRESHOLD string-keyed entries Ôćĺ a phf::Map, looked up by raw key.
    let big: Vec<(&str, &str)> = (0..30)
      .map(|i| {
        let s: &'static str = Box::leak(format!("K{i}").into_boxed_str());
        let v: &'static str = Box::leak(format!("V{i}").into_boxed_str());
        (s, v)
      })
      .collect();
    let model = TableModel {
      name: "PLUS::XMP".to_string(),
      groups: ("XMP".to_string(), "XMP-plus".to_string()),
      tags: vec![tag("Code", "Code", "string", Some(big))],
    };
    let src = emit_xmp_tables(&model_vec(model));
    assert!(src.contains("static PLUS_CODE: phf::Map<&'static str, &'static str> ="));
    assert!(src.contains("P::MapPhf(&PLUS_CODE)"));
  }

  fn model_vec(m: TableModel) -> Vec<TableModel> {
    vec![m]
  }
}
