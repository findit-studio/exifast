  #[test]
  fn tagdef_mode_emits_generic_tagtable_vocabulary() {
    // Faithful to `%DSF::Main` (DSF.pm) + `%AAC::Main` shapes: a numeric-id
    // tag with an int-keyed string map, a numeric-id plain tag, a string-id
    // tag with a numeric-LABEL map (AAC `Channels` `2 => 2`), and a string-id
    // plain tag ‚Äî proving the generic `TagDef::new` / `PrintConvHash::direct`
    // / `TagId::{Int,Str}` rendering.
    let model = TableModel {
      name: "DSF::Main".to_string(),
      groups: ("File".to_string(), "File".to_string()),
      tags: vec![
        // int-keyed string map ‚Üí PrintConv::Hash(direct), key STRINGIFIED.
        tag("4", "FormatID", "int32u", Some(vec![("0", "DSD Raw")])),
        // plain numeric-id tag ‚Üí PrintConv::None.
        tag("6", "ChannelCount", "int32u", None),
        // string-id tag with a MIXED-type map (the real AAC `Channels` shape:
        // bare numbers `1 => 1` AND strings `6 => '5+1'`) ‚Üí per-entry
        // PrintValue::I64 vs PrintValue::Str, faithful to the Perl scalars.
        tag(
          "Bit023-025",
          "Channels",
          "?",
          Some(vec![("1", "1"), ("2", "2"), ("6", "5+1")]),
        ),
        // plain string-id tag ‚Üí TagId::Str + PrintConv::None.
        tag("Encoder", "Encoder", "?", None),
      ],
    };
    let src = emit_tagdef_table(&model);

    // Provenance header + the generic `tagtable` import (NOT the XMP `Field`).
    // This table HAS value-maps ‚Üí the full import including PrintConvHash/Value.
    assert!(src.contains("// generated by xtask gen-tables from exiftool -listx"));
    assert!(src.contains(
      "use crate::tagtable::{PrintConv, PrintConvHash, PrintValue, TagDef, TagId, ValueConv};"
    ));

    // int-keyed string map ‚Üí a `&[(&str, PrintValue)]` const (key stringified,
    // const named `<NAME>_MAP`) + a `PrintConv::Hash(PrintConvHash::direct(&‚Ä¶))`.
    assert!(src.contains(
      r#"static MAIN_FORMATID_MAP: &[(&str, PrintValue)] = &[("0", PrintValue::Str("DSD Raw"))];"#
    ));
    assert!(src.contains(
      r#"TagDef::new("FormatID", "File", ValueConv::None, PrintConv::Hash(PrintConvHash::direct(&MAIN_FORMATID_MAP)))"#
    ));

    // MIXED-type map ‚Üí per-entry PrintValue: numeric labels `I64`, the `"5+1"`
    // label `Str` ‚Äî exactly the hand-written `AAC::Main` `Channels` shape.
    assert!(src.contains(
      r#"static MAIN_CHANNELS_MAP: &[(&str, PrintValue)] = &[("1", PrintValue::I64(1)), ("2", PrintValue::I64(2)), ("6", PrintValue::Str("5+1"))];"#
    ));

    // plain tags ‚Üí PrintConv::None.
    assert!(
      src.contains(r#"TagDef::new("ChannelCount", "File", ValueConv::None, PrintConv::None)"#)
    );
    assert!(src.contains(r#"TagDef::new("Encoder", "File", ValueConv::None, PrintConv::None)"#));

    // The `get` fn keys numeric ids by `TagId::Int`, string ids by `TagId::Str`;
    // the static is named by the NAME, the match key is the faithful id.
    assert!(src.contains("pub fn get(id: TagId) -> Option<&'static TagDef> {"));
    assert!(src.contains("TagId::Int(4) => Some(&MAIN_FORMATID),"));
    assert!(src.contains("TagId::Int(6) => Some(&MAIN_CHANNELCOUNT),"));
    assert!(src.contains(r#"TagId::Str("Bit023-025") => Some(&MAIN_CHANNELS),"#));
    assert!(src.contains(r#"TagId::Str("Encoder") => Some(&MAIN_ENCODER),"#));
    assert!(src.contains("_ => None,"));
  }

