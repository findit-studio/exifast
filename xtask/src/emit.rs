  #[test]
  fn decode_listx_id_bytes_handles_escapes() {
    // Plain ASCII id → its UTF-8 bytes.
    assert_eq!(decode_listx_id_bytes("GoPr"), b"GoPr");
    // ExifTool escapes a non-printable / non-ASCII byte as the literal `\xNN`.
    assert_eq!(
      decode_listx_id_bytes("FOV\\x00"),
      vec![0x46, 0x4f, 0x56, 0x00]
    );
    assert_eq!(
      decode_listx_id_bytes("\\xa9gpt"),
      vec![0xa9, 0x67, 0x70, 0x74]
    );
    // A trailing lone backslash (no `xNN`) is kept verbatim.
    assert_eq!(decode_listx_id_bytes("ab\\"), vec![b'a', b'b', b'\\']);
  }

  #[test]
  fn quicktime_map_emits_4cc_and_string_tables() {
    // Faithful to the `%QuickTime::UserData` / `%QuickTime::Keys` shapes: a
    // plain-ASCII 4cc, a NUL-bearing 4cc, a `©`-prefixed 4cc (UserData), and a
    // string key (Keys) — proving the allowlist filter, the `\xNN`-id decode,
    // and the `ConvlessAtom` rendering.
    let userdata = TableModel {
      name: "QuickTime::UserData".to_string(),
      groups: ("QuickTime".to_string(), "UserData".to_string()),
      tags: vec![
        tag("GoPr", "GoProType", "string", None),
        tag("FOV\\x00", "FieldOfView", "string", None),
        tag("\\xa9mal", "MakerURL", "string", None),
        // Present in the table but NOT in the allowlist → must be OMITTED.
        tag("CAME", "SerialNumberHash", "string", None),
      ],
    };
    let keys = TableModel {
      name: "QuickTime::Keys".to_string(),
      groups: ("QuickTime".to_string(), "Keys".to_string()),
      tags: vec![tag("direction.facing", "CameraDirection", "?", None)],
    };
    let ud_allow = [
      QuickTimeCandidate { name: "GoProType" },
      QuickTimeCandidate {
        name: "FieldOfView",
      },
      QuickTimeCandidate { name: "MakerURL" },
    ];
    let keys_allow = [QuickTimeCandidate {
      name: "CameraDirection",
    }];
    let unported = ["SerialNumberHash", "AndroidCaptureFPS"];
    let src = emit_quicktime_map(&userdata, &keys, &ud_allow, &keys_allow, &unported);

    // Provenance header + the parent-module `ConvlessAtom` import.
    assert!(src.contains("// generated by xtask gen-tables from exiftool -listx"));
    assert!(src.contains("use super::ConvlessAtom;"));

    // UserData: 4cc-byte keys, the `\xNN`/`\x00` ids decoded to raw bytes.
    assert!(src.contains("pub static QUICKTIME_USERDATA_CONVLESS: &[ConvlessAtom<&[u8]>] = &["));
    assert!(src.contains(r#"ConvlessAtom::new(&[0x47, 0x6f, 0x50, 0x72], "GoProType"),"#));
    assert!(src.contains(r#"ConvlessAtom::new(&[0x46, 0x4f, 0x56, 0x00], "FieldOfView"),"#));
    assert!(src.contains(r#"ConvlessAtom::new(&[0xa9, 0x6d, 0x61, 0x6c], "MakerURL"),"#));
    // The non-allowlisted `CAME` is NOT emitted as a map entry.
    assert!(!src.contains(r#""SerialNumberHash"),"#));

    // Keys: string keys.
    assert!(src.contains("pub static QUICKTIME_KEYS_CONVLESS: &[ConvlessAtom<&str>] = &["));
    assert!(src.contains(r#"ConvlessAtom::new("direction.facing", "CameraDirection"),"#));

    // The unported (conv/priority-bearing) candidates are documented.
    assert!(src.contains("pub static UNPORTED: &[&str] = &["));
    assert!(src.contains(r#"  "SerialNumberHash","#));
    assert!(src.contains(r#"  "AndroidCaptureFPS","#));
  }

